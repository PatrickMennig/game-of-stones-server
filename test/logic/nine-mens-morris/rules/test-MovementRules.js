const assert             = require('assert');


describe('MovementRules', function () {

    describe('isValidMove', function () {

        it('should be a valid move, placing a token on an empty field');

        it('should be a valid move, moving a token');

        it('should be a valid move, flying a token');

        it('should not be a valid move, placing on occupied field');

        it('should not be a valid move, moving on occupied field');

        it('should not be a valid move, moving on not adjacent field');

        it('should not be a valid move, flying on occupied field');

    });

});